#include<iostream>
using namespace std;

main()
{
    int digit;

    cout<<"enter your number=";
    cin>>digit;

    switch(digit)

    {

    case 0:
        cout<<"Zero";
        break;
    case 1:
        cout<<"one";
        break;
    case 2:
        cout<<"two";
        break;
    case 3:
        cout<<"three";
        break;
    case 4:
        cout<<"four";
        break;
    case 5:
        cout<<"five";
        break;
    case 6:
        cout<<"six";
        break;
    case 7:
        cout<<"seven";
        break;
        case 8:
        cout<<"eight";
        break;
    case 9:
        cout<<"Nine";
        break;
    default:
        cout<<"Not a digit in range";



    }


}
