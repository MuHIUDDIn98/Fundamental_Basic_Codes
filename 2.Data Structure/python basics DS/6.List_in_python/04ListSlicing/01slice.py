# Problem: Given numbers = [10, 20, 30, 40, 50, 60],
# extract the elements from index 1 up to (but not including) index 4. Solution

numbers = [10, 20, 30, 40, 50, 60]
print(numbers[1:4])

# Problem: Using numbers from Q1, extract the first three elements using a slice. Solution:
print(numbers[:3])

# Problem: Using numbers from Q1, extract all elements from index 3 to the end of the list.
print(numbers[3:]) # Output: [40, 50, 60]

