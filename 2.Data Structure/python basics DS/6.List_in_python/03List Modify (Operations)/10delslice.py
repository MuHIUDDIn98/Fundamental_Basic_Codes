# Problem: Create a list my_stuff = ["book", "laptop", "keys", "wallet"]. 
# Use del to remove the items from index 1 up to (but not including) index 3. Solution:

my_stuff = ["book", "laptop", "keys", "wallet"]
del my_stuff[1:3] # Removes 'laptop' and 'keys'
print(my_stuff) # Output: ['book', 'wallet']