
# Problem: items_to_process = ["A", "B", "C", "D"]. Remove and get the last item using pop(). 
# Print the item and the modified list. Solution:
items_to_process = ["A", "B", "C", "D"]
last_item = items_to_process.pop()
print(f"Removed item: {last_item}")    # Output: Removed item: D
print(f"Remaining list: {items_to_process}") # Output: Remaining list: ['A', 'B', 'C']